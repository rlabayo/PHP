$date = "02/09/1999";
$pattern = "#(?:^[1-9]{1}/[1-9]{1}/[0-9]{4}$|^0[1-9]{1}/[1-9]{1}/[0-9]{4}$|^1[012]{1}/[1-9]{1}/[0-9]{4}$|^[1-9]{1}/0[1-9]{1}/[0-9]{4}$|^[1-9]{1}/1[0-9]{1}/[0-9]{4}$|^[1-9]{1}/2[0-9]/[0-9]{4}$|^[1-9]{1}/3[01]/[0-9]{4}$|^0[1-9]{1}/0[1-9]{1}/[0-9]{4}$|^0[1-9]{1}/1[0-9]{1}/[0-9]{4}$|^0[1-9]{1}/2[0-9]{1}/[0-9]{4}$|^0[1-9]{1}/3[01]/[0-9]{4}$|^1[012]{1}/0[1-9]{1}/[0-9]{4}$|^1[012]{1}/1[0-9]{1}/[0-9]{4}$|^1[012]{1}/2[0-9]{1}/[0-9]{4}$|^1[012]{1}/3[01]{1}/[0-9]{4}$)#";
$result = preg_match($pattern, $date);

// echo the result
echo "Result: " . $result;

